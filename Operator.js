var x = 10;
var y = 20;
// Arithmetic operators 
console.log("Arithmetic operator");
console.log(x + y);
console.log(y - x);
console.log(y * x);
console.log(y / x);
console.log(y % x);
console.log(Math.pow(y, x));
// increment decrement
console.log("increment and decrement");
x++; //++x;
y--; //--y
console.log(x);
console.log(y);
// Assignment operator
console.log("Assignment operator");
x = 100;
y = 50;
console.log(x += y);
console.log(x -= y);
console.log(x *= y);
console.log(x /= y);
console.log(x %= y);
console.log(x = Math.pow(x, y));
// Relational operator
console.log("Relational Operator");
x = 5, y = 15;
console.log(x > y);
console.log(x < y);
console.log(x == y);
console.log(x >= y);
console.log(x <= y);
console.log(x != y);
// ternary operator
console.log("ternary operator");
x = 4, x = 6;
console.log(x > y ? x : y);
console.log(y > x ? y : x);
// Logical operator
console.log("Logical operator");
var a = true;
var b = false;
console.log(a && b);
console.log(a || b);
console.log(!a);
console.log(!b);
